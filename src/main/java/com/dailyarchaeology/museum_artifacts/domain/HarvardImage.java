package com.dailyarchaeology.museum_artifacts.domain;

public class HarvardImage {
	private String altText;
	private String baseImageUrl;
	private String copyright;
	private String description;
	private Integer displayOrder;
	private String format;
	private Integer height;
	private Integer idsId;
	private String iiifBaseUri;
	private Integer imageId;
	private String publicCaption;
	private String renditionNumber;
	private String date;
	private String technique;
	private Integer width;

	public HarvardImage() {};

	public String getAltText() {
		return altText;
	}

	public void setAltText(String altText) {
		this.altText = altText;
	}

	public String getBaseImageUrl() {
		return baseImageUrl;
	}

	public void setBaseImageUrl(String baseImageUrl) {
		this.baseImageUrl = baseImageUrl;
	}

	public String getCopyright() {
		return copyright;
	}

	public void setCopyright(String copyright) {
		this.copyright = copyright;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public Integer getDisplayOrder() {
		return displayOrder;
	}

	public void setDisplayOrder(Integer displayOrder) {
		this.displayOrder = displayOrder;
	}

	public String getFormat() {
		return format;
	}

	public void setFormat(String format) {
		this.format = format;
	}

	public Integer getHeight() {
		return height;
	}

	public void setHeight(Integer height) {
		this.height = height;
	}

	public Integer getIdsId() {
		return idsId;
	}

	public void setIdsId(Integer idsId) {
		this.idsId = idsId;
	}

	public String getIiifBaseUri() {
		return iiifBaseUri;
	}

	public void setIiifBaseUri(String iiifBaseUri) {
		this.iiifBaseUri = iiifBaseUri;
	}

	public Integer getImageId() {
		return imageId;
	}

	public void setImageId(Integer imageId) {
		this.imageId = imageId;
	}

	public String getPublicCaption() {
		return publicCaption;
	}

	public void setPublicCaption(String publicCaption) {
		this.publicCaption = publicCaption;
	}

	public String getRenditionNumber() {
		return renditionNumber;
	}

	public void setRenditionNumber(String renditionNumber) {
		this.renditionNumber = renditionNumber;
	}

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public String getTechnique() {
		return technique;
	}

	public void setTechnique(String technique) {
		this.technique = technique;
	}

	public Integer getWidth() {
		return width;
	}

	public void setWidth(Integer width) {
		this.width = width;
	}
}
